namespace Fasetto.Word.Core
{
    /// <summary>
    /// Horizontal alignment value for an element
    /// </summary>
    public enum ElementHorizontalAlignment
    {
        /// <summary>
        /// Align to the left
        /// </summary>
        Left = 0,

        /// <summary>
        /// Align to the center
        /// </summary>
        Center = 1,

        /// <summary>
        /// Align to the right
        /// </summary>
        Right = 2,

        /// <summary>
        /// Fills the width
        /// </summary>
        Stretch = 3
    }
}
